namespace IVRCommon.Keyboard.Enum
{
    /// <summary>
    /// 按键类型
    /// </summary>
    public enum KeyCodeType
    {
        /// <summary>
        /// 普通字母按键
        /// </summary>
        Letter,
        /// <summary>
        /// 数字按键
        /// </summary>
        Digit,
        /// <summary>
        /// 特殊符号
        /// </summary>
        Symbol,
        /// <summary>
        /// 输入空格
        /// </summary>
        Space,
        /// <summary>
        /// 返回
        /// </summary>
        Back,
        /// <summary>
        /// 删除最后一个
        /// </summary>
        DeleteLast,
        /// <summary>
        /// 切换大小写 Upper or  lower
        /// </summary>
        UpperLower,
        /// <summary>
        /// 切换符号
        /// </summary>
        SwitchSymbol,

        SwitchLetter,

        SwitchDigit,

        /// <summary>
        /// 确认键
        /// </summary>
        Sure,
        /// <summary>
        /// 日文按键
        /// </summary>
        JaPan,
        /// <summary>
        /// 日文按键
        /// </summary>
        SwitchJaPan,
        /// <summary>
        /// 切换键盘类型按钮
        /// </summary>
        SwitchKeyboardButton,
        /// <summary>
        /// 关闭键盘
        /// </summary>
        CloseKeyboard

    }
}
